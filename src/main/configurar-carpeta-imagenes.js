import { app } from 'electron'
import path from 'path'
import fs from 'fs'

// APUNTES
// PARA ALMACENAR LAS IMAGENES DE LOS PRODUCTOS SIN GUARDARLAS DIRECTAMENTE EN LA BASE DE DATOS
// CREO UNA CARPETA QUE LAS CONTENDRÁ, ESTA CARPETA DEBE GUARDARSE LOCALMENTE EN EL PC (APPDATA)
// COMO ELECTRON (CHROMIUM) POR DEFECTO IMPIDE LA CARGA DE IMAGENES LOCALES NO SE PUEDE USAR src="C:/users(foto.jpg" ni file://users/foto.jpg
// POR LO TANTO PARA CARGAR IMAGENES SIN DESACTIVAR LA PROTECCION CON webSecurity: false, HAY QUE CREAR UN PROTOCOLO PERSONALIZADO QUE PERMITA LA CARGA DE ARCHIVOS BAJO CIERTO CRITERIO
// BÁSICAMENTE EL PROTOCOLO CONSISTE EN EJECUTAR UN HANDLE EN EL PROTOCOLO (IMPORTADO Y EJECUTADO EN INDEX.JS DE MAIN) QUE OBTENDRÁ LA RUTA DE UN ARCHIVO Y CREARÁ UN PREFIJO PARA UTILIZARLO EN EL FRONTEND

// PARA ESTO, CREAMOS UNA CONSTANTE QUE APUNTARÁ A LA CARPETA DONDE SE GUARDARÁN LAS IMAGENES USANDO userData (la carpeta de la app dentro de AppData) Y CON path.join CREAMOS LA RUTA A LA NUEVA CARPETA
export const CARPETA_IMAGENES = path.join(app.getPath('userData'), 'imagenes_productos')

// LUEGO REVISAMOS LA EXISTENCIA DE LA CARPETA, DE LO CONTRARIO LA CREAMOS, ESTO DEBE SER ANTES DE EJECUTAR EL PROTOCOLO
export function inicializarDirectorio() {
  if (!fs.existsSync(CARPETA_IMAGENES)) {
    fs.mkdirSync(CARPETA_IMAGENES, { recursive: true })
  }
}

// FINALMENTE, ESTABLECEMOS EL PROTOCOLO Y NET ACA RECIBIDO COMO PARAMETRO DESDE INDEX.JS EN MAIN
// HACEMOS EL HANDLE CON EL CANAL 'app-img' QUE REEMPLAZARÍA AL file:// EN EL FRONTEND
// NET ES EL MODULO NATIVO DE ELECTRON QUE PERMITE LAS PETICIONES DE RED USANDO CHROMIUM
// NET ES EL QUE REALMENTE TOMA LOS BYTES DEL ARCHIVO EN LA RUTA Y EJECUTA EL FETCH CON LAS CABECERAS NECESARIAS Y DEVUELVE LOS DATOS
export function configurarProtocoloImagenes(protocol, net) {
  protocol.handle('app-img', (request) => {
    const url = new URL(request.url)
    const nombreArchivo = url.hostname + url.pathname
    const rutaCompleta = path.join(CARPETA_IMAGENES, nombreArchivo)

    return net.fetch(`file://${rutaCompleta}`)
  })
}
